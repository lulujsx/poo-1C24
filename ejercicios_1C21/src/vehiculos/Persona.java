package vehiculos;

public class Persona {
	
	// Atributos
	private String nombre;
	private int dni;
	
	// Constructor
	
	public Persona(String nombre, int dni) {
		this.nombre = nombre;
		this.dni = dni;
	}
	
	// Metodos
	
	public String getNombre() {
		return this.nombre;
	}
	
	public int getDni() {
		return this.dni;
	}
	
	

}
